import main

from queue import PriorityQueue


class Solution:
    def eatenApples(self, apples: list[int], days: list[int]) -> int:
        count = 0
        bag = PriorityQueue()  # tuples of day expire on and num apples

        def eat_today():
            nonlocal count, bag
            while True:
                if bag.empty():
                    break
                expireOn, numApples = bag.get()
                if idx >= expireOn:
                    continue  # Expired
                # Eat
                count += 1
                if numApples > 1:
                    bag.put((expireOn, numApples - 1))
                break

        # Add apples to bag
        # Eat from earliest to expire apple bunch
        for idx in range(len(apples)):
            if apples[idx] != 0:
                bag.put((idx + days[idx], apples[idx]))
            eat_today()

        idx = len(apples)
        while not bag.empty():
            eat_today()
            idx += 1
        return count


